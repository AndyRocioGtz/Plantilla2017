                <label class="titulo">Dirección de Notarías</label><br><br>

                <label><strong>Misión</strong></label><br>
                Contribuir con apego al marco legal a reunir, conservar, organizar y facilitar los
                documentos que constituyen el Patrimonio Documental Notarial del Estado de Guanajuato,
                mediante el uso de infraestructura tecnológica de vanguardia, así como vigilar objetiva
                y profesionalmente las actividades del Notario Público, para lograr la satisfacción
                total en la prestación de nuestros servicios, garantizando certeza jurídica y paz social, con
                la integración de una mejora continua en nuestros procesos.  <br><br>

                 <label><strong>Visión</strong></label><br>
                Brindar servicios de Excelencia, en la Dirección y control de la función y trámites
                notariales y del Archivo de Notarias, con verdadero sentido humanista,
                comprometidos a preservar la paz y justicia buscando ser modelo a nivel nacional
                sobre la base de los valores que repercutan positivamente en la calidad de los
                servicios investidos de certeza jurídica, en beneficio de la satisfacción ciudadana
                y con el uso de tecnología de punta con el compromiso de buscar una mejora continua permanente.<br><br>

                <label><strong>Políticas de Calidad</strong></label><br>
                Coordinar la función notarial a través de la mejora continua para garantizar
                certeza y seguridad jurídica.. <br><br><br>

                 <label><strong>Valores</strong></label><br>
                    Base de los principios básicos de nuestras decisiones y acciones diarias,
                    ayudándonos a realizar nuestra visión compartida y cumplir con nuestra misión,
                    con la aplicación misma de la política de calidad. Demostrando con esto el
                    compromiso a los más altos estándares de servicio, Dirección y Humanismo, con un
                    ambiente laboral seguro donde los valores sean suministrados con respeto y dignidad.<br></p>
