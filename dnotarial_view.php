              <div class="sec2-tramite"><br><br>
                <label><strong>Misión</strong></label><br>
                Contribuir con apego al marco legal a reunir, conservar, organizar y facilitar los 
                documentos que constituyen el Patrimonio Documental Notarial del Estado de Guanajuato,<br> 
                mediante el uso de infraestructura tecnológica de vanguardia, así como vigilar objetiva 
                y profesionalmente las actividades del Notario Público, para lograr la satisfacción <br>
                total en la prestación de nuestros servicios, garantizando certeza jurídica y paz social, con 
                la integración de una mejora continua en nuestros procesos.  <br><br><br>

                 <label><strong>Visión</strong></label><br>
                Brindar servicios de Excelencia, en la Dirección y control de la función y trámites
                notariales y del Archivo de Notarias, con verdadero sentido humanista, 
                comprometidos <br>a preservar la paz y justicia buscando ser modelo a nivel nacional
                sobre la base de los valores que repercutan positivamente en la calidad de los 
                servicios investidos <br>de certeza jurídica, en beneficio de la satisfacción ciudadana 
                y con el uso de tecnología de punta con el compromiso de buscar una mejora continua permanente.  <br>
              </div>
             </div> 
