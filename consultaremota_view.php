                    <label class="titulo">Consulta Remota</label>
                    <p align="justify">
                        El servicio de Consulta Remota de la Dirección de Registros Públicos de la Propiedad
                        y del Comercio, es un servicio para usuarios registrados, en donde, a través de
                        nuestro portal web, es posible consultar datos sobre los asientos registrales
                        del sistema SEGURE.
                        <br>
                        <br>
                        Por medio de este, los usuarios podrán consultar información del Registro Público,
                        sin necesidad de acudir a una de las <br> oficinas del mismo.
                        <br>
                        <br>
                        <strong> Beneficios</strong>
                        <li>Búsquedas confiables y con mayor rapidez. </li>
                        <li>Ahorro de tiempo, dinero y traslado a los Registros Públicos del Estado de Guanajuato.</li>
                        <li>El manejo del portal, es sencillo y amigable.</li>
                        <br>

                        <strong>Requisitos para nueva subscripción (anual)</strong>
                        <li>Comprobante de domicilio.</li>
                        <li>Comprobante de pago por la cantidad de $ 1,308.00, mediante el <a href="https://pagosenlinea.guanajuato.gob.mx/public/seleccionarServicio?servicio=DD19"
