                <label class="titulo">Dirección de Registros Públicos</label><br><br>
                El Registro Público de la Propiedad del Estado de Guanajuato es una institución de la Secretaría 
                de Gobierno del Estado de Guanajuato que tiene como fin dar publicidad a los actos que conforme a 
                la ley lo requieran para surtir efectos contra terceros. <br><br>
                Con el fin de brindar un mejor servicio en este portal se pone a su disposición la información 
                necesaria para conocer y realizar cualquier trámite ante el Registro Públic de la Propiedad y 
                de Registro Público de Comercio.<br><br><br>


                <label><strong>Misión</strong></label><br>
                Prestar servicios que garanticen publicidad y seguridad jurídica de los actos registrales, 
                de conformidad con la legislación aplicable, de manera eficiente, ética y eficaz, en beneficio 
                de la sociedad y la paz social. <br><br><br>

                 <label><strong>Visión</strong></label><br>
                Ser una institución registral, que sea modelo nacional e internacional preservando el 
                sentido humano, las aspiraciones de paz social y seguridad jurídica, con tecnología de 
                vanguardia y en un marco jurídico actualizado. <br>
